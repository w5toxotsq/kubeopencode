// Helper to build paginated list responses
export function paginateList<T>(items: T[], limit: number, offset: number) {
  const slice = items.slice(offset, offset + limit);
  return {
    items: slice,
    pagination: {
      limit,
      offset,
      totalCount: items.length,
      hasMore: offset + limit < items.length,
    },
  };
}

